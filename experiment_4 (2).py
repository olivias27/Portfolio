import stanza
import pandas as pd
stanza.download('en')
nlp = stanza.Pipeline('en', processors='tokenize,pos')

"""Reading in the previous BL results to a pandas df. Because the BL data is stored in a TSV (where data is divided by tabs, representd by \t), the separation must be clarified."""

df = pd.read_csv('/content/sample_data/Bulwer-Lytton.tsv', sep = '\t')

"""Method to extract Adjective-Noun pairs from sentences:
* Parameters: text (raw string or document)

1. Returns empty list if document is empty or NaN
2. nlp maps all data into a specific hiearchy
    * doc.sentences: all  sentence objects found
    * s.words: all tokenized word objects in that specific sentence
3. For each sentence, we loop through every word.
    * If the current word is labeled as an ADJ and the next as NOUN, the pair will be converted to lower case and appended to the pairs list
"""

def extract_an_pairs(text):
  doc = nlp('\n\n'.join(text))

  pairs = []
  for s in doc.sentences:
    words = [word.text for word in s.words]
    pos = [word.upos for word in s.words]
    for i in range(len(s.words) - 1):
      if pos[i] == 'ADJ' and pos[i+1] == 'NOUN':
        pair = ' '.join((words[i], words[i+1]))
        pairs.append(pair.lower())
  return pairs

def extract_an_pairs_bl(text):
  doc = nlp(text)

  pairs = []
  for s in doc.sentences:
    words = [word.text for word in s.words]
    pos = [word.upos for word in s.words]
    for i in range(len(s.words) - 1):
      if pos[i] == 'ADJ' and pos[i+1] == 'NOUN':
        pair = ' '.join((words[i], words[i+1]))
        pairs.append(pair.lower())
  return pairs

"""Works by:
1. Creating a list of all sentences from the df
2. Create a loop that cycles through all sentences, using enumerate as a counter to track progression
  * Append all extracted AN pairs from the sentence to the results list
  * Use the counter to determine progression through df
4. Save the results in a new column of the df called an_results
5. Saves the results to a CSV in order to prevent recalculations

"""

bulwer_ans = []
sentences = df['sentence'].tolist()
for i, txt in enumerate(sentences):
    bulwer_ans.append(extract_an_pairs_bl(txt))

    if i % 50 == 0 and i > 0:
        print(f"{i} sentences processed...")

df['an_pairs'] = bulwer_ans
print("Finished!")
